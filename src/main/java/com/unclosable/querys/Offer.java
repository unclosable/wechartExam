package com.unclosable.querys;

import java.math.BigDecimal;

public class Offer {

	private String compment;
	private String position;
	private BigDecimal monthPay;
	private String aliPay;
	private String contact;
	private String message;

	public String getCompment() {
		return compment;
	}

	public void setCompment(String compment) {
		this.compment = compment;
	}

	public String getPosition() {
		return position;
	}

	public void setPosition(String position) {
		this.position = position;
	}

	public BigDecimal getMonthPay() {
		return monthPay;
	}

	public void setMonthPay(BigDecimal monthPay) {
		this.monthPay = monthPay;
	}

	public String getAliPay() {
		return aliPay;
	}

	public void setAliPay(String aliPay) {
		this.aliPay = aliPay;
	}

	public String getContact() {
		return contact;
	}

	public void setContact(String contact) {
		this.contact = contact;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}

}
